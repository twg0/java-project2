package com.likelion.week2.day1;

public class DivideIntoTwo {
    public static void main(String[] args) {

        float fOne = 1.0f;
        float fTwo = 2;
        System.out.printf("%f\n", fOne + fOne / fTwo);

        int iOne = 1;
        int iTwo = 2;
        System.out.printf("%d\n", iOne + iOne / iTwo);

        System.out.println(2.0 == 1 + 1.0);

        System.out.printf("%d\n", 9 / 2);
        System.out.printf("%f\n", 9 / 2.0);

    }
}
