package com.likelion.week3.day1;

public class LoopChar {
    public static void main(String[] args) {

        for (char c = 'a'; c <= 'z';c++) {
            System.out.printf("%c ", c);
        }
        System.out.println();
        for (char c = 'a'; c <= 'z';c++) {
            System.out.printf("%c\n", c);
        }

        System.out.printf("%c\n", 222222222);
    }
}
